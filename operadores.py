# Operadores Aritméicos:
# + adição 2+3 = 5
# - subtração 7-5 = 2
# * multiplicação 4*3 = 12
# / divisão 10/2 = 5
# // divisão inteira 10//3 = 3
# % módulo(resto da divisão) 10 % 3 = 1
# ** exponenciação 2**3 = 8

n1 = float(input('Digite um número: '))
n2 = float(input('Digite outro número: '))

print(f'A muliplicação de {n1} e {n2} é {n1*n2}')

print(f'A divisão de {n1} e {n2} é {n1/n2}')

print(f'A divisão inteira de {n1} e {n2} é {n1//n2}')

print(f'O módulo de {n1} e {n2} é {n1%n2}')

print(f'A exponencição de {n1} e {n2} é {n1**n2}')